using System;

namespace _06._Reversed_Chars
{
    class Program
    {
        static void Main(string[] args)
        {
            char x = char.Parse(Console.ReadLine());
            char y = char.Parse(Console.ReadLine());
            char z = char.Parse(Console.ReadLine());

            Console.WriteLine($"{z} {y} {x}");
        }
    }
}
